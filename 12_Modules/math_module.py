import math

number = 25
radius = 2

print(f"Square Root : {math.sqrt(number)}")
print(f"Factorial   : {math.factorial(5)}")
print(f"Ceil        : {math.ceil(45.20)}")
print(f"Floor       : {math.floor(45.99)}")
print(f"Power       : {math.pow(2, 3)}")
print(f"Pi          : {math.pi}")

circumference = 2 * math.pi * radius
print(f"Circumference: {circumference:.2f}")